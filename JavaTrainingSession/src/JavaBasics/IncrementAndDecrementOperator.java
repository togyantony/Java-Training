package JavaBasics;

public class IncrementAndDecrementOperator {
	public static void main(String[] args) {
	
	int i=1;
	int j=i++;
	System.out.println(i);
	System.out.println(j);
	
	int a=1;
	int b=++a;
	System.out.println(a);
	System.out.println(b);
	
	int m=4;
	int n=m--;
	System.out.println(m);
	System.out.println(n);

}
	
}